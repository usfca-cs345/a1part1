(* A function for placeholder values left to students to implement.

   Depending on the needed type, you may see "todo" or "todo ()". You need to
   replace "todo ()" if you see it, otherwise replacing "todo" is enough.

   The rule of thumb is that:
   - todo () is a placeholder for any value
   - todo is a placeholder for a function value
*)
let todo _ = failwith "You need to implement this function."
